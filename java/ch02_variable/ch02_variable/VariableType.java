package ch02_variable;

public class VariableType {
	//변수의 타입(기본 타입)
	
	//정수 타입(소수점 x)
	//롱은 실수 뒤에 l or L를 붙여야 한다.)
	byte byteVar = 1;
	short shorVar = 2;
	int intVar = 3;
	long longVar = 4l;
	long longVal = 5L;
	
	//정수타입은 맞지만 숫자로는 보기 애매하다.
	char charVar = 5;
	char charVal = '짱';
	
	//실수 타입(플롯은 실수 뒤에 f or F를 붙여야 한다.)
	double doubleVar = 3.14;
	float floatVar = 3.14f;
	float floatVal = 3.14F;
	
	//불리언 타입(참/거짓, True,False)
	boolean boolVar = true;
	boolean boolVal = false;
	
	
	
	

}
